// Anonyme Funktionen in JavaScript

// 1. Anonyme Funktionen:
//    - Anonyme Funktionen sind Funktionen ohne Namen.
//    - Sie können direkt als Argumente an andere Funktionen übergeben werden.
//    - Beispiel:

let greeting = function (name) {
    return "Hallo, " + name + "!";
}

// Aufruf der anonymen Funktion
console.log(greeting("Welt")); // Ausgabe: Hallo, Welt!
